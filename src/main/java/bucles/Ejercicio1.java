package bucles;

public class Ejercicio1 {
    /*
    Muestre los números múltiplos de 5, de 0 a 100 usando un bucle for
     */
    public static void main(String[] args) {

        System.out.println("Múltiplos de 5: For \n");
        for (int i = 0; i <101 ; i+=5) {

            System.out.println(i);
        }

    }
}
