package bucles;

public class Ejercicio2 {
    public static void main(String[] args) {

           /*
    Muestre los números múltiplos de 5, de 0 a 100 usando un bucle while
     */

        int i=0;
        System.out.println("Múltiplos de 5 con While \n");
        while(i<=100){
            System.out.println(i);
            i+=5;
        }
    }
}
