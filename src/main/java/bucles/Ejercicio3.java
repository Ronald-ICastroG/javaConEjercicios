package bucles;

public class Ejercicio3 {
    public static void main(String[] args) {
          /*
    Muestre los números múltiplos de 5, de 0 a 100 usando un bucle Do-while
     */
        int i=5;
        System.out.println("Múltiplos de 5 con do while \n");
        do{
            System.out.println(i);
            i+=5;
        }while(i<=100);
    }
}
